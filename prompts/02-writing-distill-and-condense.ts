/**
 * Prompt for removing irrelevant information and overall condensing the writing. The
 * idea is to be left with only the most important and fundamental information.
 *
 * @attribute Writing improvement.
 */
export const prompt = `
Distill and condense the following passage, saving only the most important information.

In particular I am not interested in reading about any of the following:
- Side tangents or irrelevant information.
- Author commentary that is not directly related to the main point.
- Repetition of the same point in different words.
`;
